    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
        if(txtTimMa.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không để trống ô tìm kiếm!");
        }else if(!txtTimMa.getText().matches("^T\\d{3}$")){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng mã nhân viên, ví dụ đúng: NV001");
        }else{
        JOptionPane.showMessageDialog(this, "Đã tìm thấy thông tin cho mã tour "+txtTimMa.getText());
        conn=connectdb.getConnection();
        try {
            CallableStatement st=conn.prepareCall("{call sp_timtour(?)}");
            st.setString(1, txtTimMa.getText());
            ResultSet rs=st.executeQuery();
            while(rs.next()){
                txtMaTour.setText(rs.getString("MaTour").trim());
                txtTenTour.setText(rs.getString("TenTour").trim());
                txtMaDD.setText(rs.getString("MaDiaDiem").trim());
                txtThoiGian.setText(rs.getString("ThoiGian").trim());
                txtGiaNL.setText(String.valueOf(rs.getFloat("GiaNguoiLon")));
                txtGiaTE.setText(String.valueOf(rs.getFloat("GiaTreEm")));
                dcNgayBD.setDate(rs.getDate("NgayBatDau"));
                jDateChooser2.setDate(rs.getDate("NgayKetThuc"));
                txtMoTa.setText(rs.getString("MoTaTour").trim());
                uphinh(rs.getString("HinhAnhTour").trim());
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả cho "+txtTimMa.getText());
        }
        }
        btnSua.setEnabled(true);
        btnXoa.setEnabled(true);
        lblHinhTour.setEnabled(true);
        teen();
        txtTimMa.setText("");
    }//GEN-LAST:event_btnTimkiemActionPerformed

