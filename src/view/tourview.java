    public boolean checkNullandRegex() {
        if (txtMaTour.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã tour!");
            txtMaTour.requestFocus();
            return false;
        }
        if (txtMaDD.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã địa điểm!");
            txtMaDD.requestFocus();
            return false;
        }
        if (txtTenTour.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên tour!");
            txtTenTour.requestFocus();
            return false;
        }
        if (lblHinhTour.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống hình!");
            return false;
        }
        if (txtThoiGian.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống thời gian!");
            txtThoiGian.requestFocus();
            return false;
        }
        if (txtGiaNL.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống giá!");
            txtGiaNL.requestFocus();
            return false;
        }
        if (txtGiaTE.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống giá!");
            txtGiaTE.requestFocus();
            return false;
        }
        if (txtMoTa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mô tả!");
            txtMoTa.requestFocus();
            return false;
        }
        if(dcNgayBD.getDate().equals("")){
            JOptionPane.showConfirmDialog(this, "Không được để trống ngày bắt đầu!");
            return false;
        }
        if(jDateChooser2.getDate().equals("")){
            JOptionPane.showConfirmDialog(this, "Không được để trống ngày bắt đầu!");
            return false;
        }
        Date BD= dcNgayBD.getDate();
        Date KT= jDateChooser2.getDate();
        if(BD.before(KT)){
            JOptionPane.showMessageDialog(this, "Lỗi: Ngày bắt đầu phải nằm trước ngày kết thúc/Ngày kết thúc phải nằm sau ngày bắt đầu!");
            return false;
        }
        String matourregex="^T\\d{3}$";
        String soregex="^[1-9]\\d*(\\.\\d+)?$";
        if(!txtGiaNL.getText().matches(soregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Giá trị vừa được nhập vào không phải là số!");
            txtGiaNL.requestFocus();
            return false;
        }
        if(!txtGiaTE.getText().matches(soregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Giá trị vừa được nhập vào không phải là số!");
            txtGiaTE.requestFocus();
            return false;
        }
        if(!txtMaTour.getText().matches(matourregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng mã tour, ví dụ đúng: T001");
            txtMaTour.requestFocus();
            return false;
        }
        return true;
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkNullandRegex()){
        int confirm = JOptionPane.showConfirmDialog(this, "Thêm tour "+txtTenTour.getText()+"", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
        tour tr = new tour();
        tr.setMaTour(txtMaTour.getText());
        tr.setTenTour(txtTenTour.getText());
        tr.setMaDiaDiem(txtMaDD.getText());
        ImageIcon icon = (ImageIcon) lblHinhTour.getIcon();
        tr.setHinhAnhTour(icon == null ? null : lblHinhTour.getText());
        tr.setThoiGian(txtThoiGian.getText());
        tr.setGiaNguoiLon(Float.parseFloat(txtGiaNL.getText()));
        tr.setGiaTreEm(Float.parseFloat(txtGiaTE.getText()));
        tr.setNgayBatDau(new java.sql.Date(dcNgayBD.getDate().getTime()));
        tr.setNgayKetThuc(new java.sql.Date(jDateChooser2.getDate().getTime()));
        tr.setMoTaTour(txtMoTa.getText());

        try {
            toursc.them(tr);
            lst.add(tr);
            displayToTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        vh();
        fen();
        }
        }
    }//GEN-LAST:event_btnThemActionPerformed

