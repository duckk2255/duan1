    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkNullandRegex()) {
            int confirm = JOptionPane.showConfirmDialog(this, "Thêm dịch vụ "+txtTen.getText()+" ?", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
            DichVu dv = getDataForm();
            try {
                dvs.add(dv);
                listDV.add(dv);
                loadTable(listDV);
                JOptionPane.showMessageDialog(this, "Thêm dịch vụ thành công");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
            vh();
            fen();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (checkNullandRegex()) {
            int confirm = JOptionPane.showConfirmDialog(this, "Sửa thông tin dịch vụ "+txtTen.getText()+"", "Xác nhận sửa", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
            int selectedRow = tabData.getSelectedRow();
                DichVu dv = getDataForm();
                if (dvs.change(dv)) {
                    listDV.set(selectedRow, dv);
                    loadTable(listDV);
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại");
                }
                vh();
                fen();
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tabData.getSelectedRow();
            String maDV = (String) dtm.getValueAt(selectedRow, 0); // Thay đổi từ int sang String
            int confirm = JOptionPane.showConfirmDialog(this, "Xóa dịch vụ "+txtTen.getText()+" ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                dvs.delete(maDV);
                listDV.remove(selectedRow);
                loadTable(listDV);
                JOptionPane.showMessageDialog(this, "Xóa dịch vụ thành công");
                vh();
                fen();
            }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
            
        String searchText = txtSearch.getText().trim();
            if (!searchText.isEmpty()) {
                String maDV = searchText;
                DichVu dv = dvs.findById(maDV);
                if (dv != null) {
                    // Clear existing data in table
                                JOptionPane.showMessageDialog(this, "Đã tìm thấy thông tin cho "+txtSearch.getText());
                    listDV.clear();
                    listDV.add(dv);
                    loadTable(listDV);
                    dtm.setRowCount(0);
                    // Add the found item to table
                    dtm.addRow(new Object[]{
                        dv.getMaDV(),
                        dv.getTenDV(),
                        dv.getMoTaDV(),
                        dv.getGiaDV()
                    });
                    // Update details in the text fields
                    detailTable(0); // Since there's only one item, use index 0
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả cho "+txtSearch.getText());
                    // Clear table if no item found
                    dtm.setRowCount(0);
                    // Clear detail fields
                    txtMa.setText("");
                    txtTen.setText("");
                    txtMota.setText("");
                    txtGia.setText("");
                }
            }else if(!searchText.matches("^DV\\d{3}$")){
                JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng mã dịch vụ, ví dụ đúng: DV001");
            }else {
                JOptionPane.showMessageDialog(this, "Không để trống ô tìm kiềm.");
            }
            btnSua.setEnabled(true);
        btnXoa.setEnabled(true);
        teen();
        txtSearch.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed

