    public void loadTable(ArrayList<DichVu> list) {

        ArrayList<DichVu> dv = dvs.findAll();
        dtm.setRowCount(0);
        for (DichVu dV : dv) {
            dtm.addRow(new Object[]{
                dV.getMaDV(),
                dV.getTenDV(),
                dV.getMoTaDV(),
                dV.getGiaDV()});
        }
    }

    public void detailTable(int index) {
        if (index >= 0 && index < listDV.size()) {
            DichVu dv = listDV.get(index);
            txtMa.setText(String.valueOf(dv.getMaDV()));
            txtTen.setText(dv.getTenDV());
            txtMota.setText(dv.getMoTaDV());
            txtGia.setText(String.valueOf(dv.getGiaDV()));
        }
    }

    public DichVu getDataForm() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String moTa = txtMota.getText();
        String gia = txtGia.getText();
        return new DichVu(ma, ten, moTa, Float.parseFloat(gia));
    }

    public boolean checkNullandRegex() {
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã dịch vụ");
            txtMa.requestFocus();
            return false;
        }
        if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên dịch vụ");
            txtTen.requestFocus();
            return false;
        }
        if (txtMota.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mô tả");
            txtMota.requestFocus();
            return false;
        }
        if (txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống giá");
            txtGia.requestFocus();
            return false;
        }
        String soregex="^[1-9]\\d*(\\.\\d+)?$";
        String makhregex="^DV\\d{3}$";
        if (!txtMa.getText().matches(makhregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng mã dịch vụ, ví dụ đúng: DV001");
            txtMa.requestFocus();
            return false;
        }
        if(!txtGia.getText().matches(soregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Giá trị vừa được nhập vào không phải là só!");
            txtGia.requestFocus();
            return false;
        }
        return true;
    }
