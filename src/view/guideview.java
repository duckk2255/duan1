    void fillTable(ArrayList<guide> hdv) {
        dtm = (DefaultTableModel) tblQliHDV.getModel();
        dtm.setRowCount(0);
        for (guide huongDanVienn : hdv) {
            Object[] row = {
                huongDanVienn.getMaHDV(),
                huongDanVienn.getMaTour(),
                huongDanVienn.getTenHDV(),
                huongDanVienn.getSoDienThoaiHDV(),
                huongDanVienn.getEmailHDV(),};
            dtm.addRow(row);
        }
    }
