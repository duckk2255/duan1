    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(checkNullandRegex()){
            String maHDV = txtMa.getText().trim();
        String maTour = txtMaTour.getText().trim();
        String tenHDV = txtTen.getText().trim();
        String sdtHDV = txtSDT.getText().trim();
        String emailHDV = txtEmail.getText().trim();
        if (maHDV.length() == 0 || maTour.length() == 0 || tenHDV.length() == 0 || sdtHDV.length() == 0 || emailHDV.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không đươc để trống");
            return;
        }
        guide hdvien = new guide(maHDV, maTour, tenHDV, Integer.parseInt(sdtHDV), emailHDV);
        repo.Them(hdvien);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        fillTable(repo.fillAll());
        vh();
        fen();
        }
    }//GEN-LAST:event_btnThemActionPerformed

