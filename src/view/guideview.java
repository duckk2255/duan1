    public boolean checkNullandRegex(){
        if (txtMa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã guide!");
            txtMa.requestFocus();
            return false;
        }
        if (txtMaTour.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống mã tour!");
            txtMaTour.requestFocus();
            return false;
        }
        if (txtTen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên guide!");
            txtTen.requestFocus();
            return false;
        }
        if (txtEmail.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống email guide!");
            txtEmail.requestFocus();
            return false;
        }
        if (txtSDT.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống sdt guide!");
            txtSDT.requestFocus();
            return false;
        }
        String sdtregex= "^0\\d{9}$";
        String emailregex= "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";
        String mahdregex="^HDV\\d{3}$";
        if(!txtMa.getText().matches(mahdregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng mã guide, ví dụ đúng: HD001");
            txtMa.requestFocus();
            return false;
        }
        if(!txtEmail.getText().matches(emailregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng email!");
            txtEmail.requestFocus();
            return false;
        }
        if(!txtSDT.getText().matches(sdtregex)){
            JOptionPane.showMessageDialog(this, "Lỗi: Sai định dạng sdt!");
            txtSDT.requestFocus();
            return false;
        }
        return true;
        
    }
